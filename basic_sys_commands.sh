#!/bin/bash

HOSTNAME=$(hostname)
CURRENT_DATE=$(date +"%Y-%m-%d %H:%M:%S")
FILE_DATE=$(date +"%Y%m%d_%H%M%S")
OUTPUT_FILE="report_${HOSTNAME}_${FILE_DATE}.txt"


{

echo "Information Gathering Report"
echo "Execution date: $CURRENT_DATE"
echo "Host name: $HOSTNAME"
echo "==================================================================="

echo "***[Processes of Kali user]***"
ps -u kali
echo "==================================================================="
echo "***[Most RAM usage]***"
ps aux --sort=-%mem | head
echo "==================================================================="
echo "***[Root user processes]***"
ps -u root u | head
echo "==================================================================="
pstree -p
echo "==================================================================="
dpkg -l | head
echo "==================================================================="
dpkg -l openssh-server
echo "==================================================================="
cat /var/log/dpkg.log | grep vsftpd
echo "==================================================================="
find /var/lib/dpkg/info/ -iname "*.list" -mtime -7 | head
echo "==================================================================="
systemctl status ssh apache2
echo "==================================================================="
ss -tulnp
echo "==================================================================="
ps aux | grep 2688
echo "==================================================================="
nmap -sV -p 22,80 192.168.31.128
echo "==================================================================="
systemctl list-units --type=service state=active | head
echo "==================================================================="
systemctl list-unit-files --type=service --state=enabled 
echo "==================================================================="
awk -F':' '{print "Użytkownik: "" " $1}' /etc/passwd
echo "==================================================================="
grep -E "(bash|sh|zsh)$" /etc/passwd
echo "==================================================================="
grep "^sudo" /etc/group
echo "==================================================================="
find /etc -type f -mtime -7 | head
echo "==================================================================="
find / -type f -size +1G 2>/dev/null
echo "==================================================================="
crontab -u root -l
echo "==================================================================="
systemctl status vsftpd
echo "==================================================================="
uname -r
echo "==================================================================="
uptime
echo "==================================================================="
free -h
echo "==================================================================="
grep -E "(cpu cores|cpu MHz)" /proc/cpuinfo
echo "==================================================================="
df -h
echo "==================================================================="
ip address show
echo "==================================================================="
cat /etc/resolv.conf
echo "==================================================================="
ip neighbor show
echo "==================================================================="
ip route show
echo "==================================================================="
iptables -L -n -v
echo "==================================================================="
aa-status
} > "$OUTPUT_FILE"


echo "Report Finished."

